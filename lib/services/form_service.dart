  Future save(f.Form form, {bool showLoad = false}) async {
    if (!Authen.isSample) {
      showLoad ? Utils.showInProgress(true) : null;
      await databaseService.dbInsert(form.formMap(), form.dbTable);

      ///Save signature
      for (int i = 0; i < form.fields.length; i++) {
        if (form.fields[i].type == FieldType.signature) {
          await form.fields[i].convertSignature((String response) {}, false);
          if (form.fields[i].signature != null) {
            await _saveSigToDevice(
                '${form.id}${form.fields[i].name}', form.fields[i].signature!);
          }
        }
      }
      showLoad ? Utils.showInProgress(false) : null;
      notifyListeners();
    }
  }

  ///TODO: New form (7): Add new form delete db
  void delete(f.Form form, void Function(String) callback) async {
    try {
      forms.removeWhere((e) => e.id == form.id);
      if (form.type == f.FormType.lineCheck) {
        await databaseService.dbDelete(form, kLineCheckTable);
      } else if (form.type == f.FormType.ppc) {
        await databaseService.dbDelete(form, kPPCTable);
      } else if (form.type == f.FormType.ppc5) {
        await databaseService.dbDelete(form, kPPC5Table);
      } else if (form.type == f.FormType.rt5) {
        await databaseService.dbDelete(form, kRt5Table);
      } else if (form.type == f.FormType.rt6) {
        await databaseService.dbDelete(form, kRt6Table);
      } else if (form.type == f.FormType.lineTrain) {
        await databaseService.dbDelete(form, klineTrainTable);
      }
      notifyListeners();
      callback(kStatusSuccess);
    } catch (e) {
      callback('$e');
    }
  }

