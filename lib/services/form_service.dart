  ///TODO: New form (5.1): Add new pdf naming
  String pdfName(f.Form form, DateTime date) {
    if (form.type == f.FormType.ppc ||
        form.type == f.FormType.ppc5 ||
        form.type == f.FormType.rt5 ||
        form.type == f.FormType.rt6) {
      String dateText = form
          .fields[form.fields.indexWhere((e) => e.name == 'check_date')]
          .stringValue
          .replaceAll(' ', '-');
      // DateTime date = form
      //     .fields[form.fields.indexWhere((e) => e.name == 'check_date')]
      //     .dateTimeValue!;
      // String dateText = DateFormat('yyyyMMdd').format(date);

      String id = idFormatted(form
          .fields[form.fields.indexWhere((e) => e.name == 'pilot_id')]
          .stringValue);
      String rank = form
          .fields[form.fields.indexWhere((e) => e.name == 'pilot_rank')]
          .stringValue
          .toUpperCase();
      String name = nameFormatted(form
              .fields[form.fields.indexWhere((e) => e.name == 'pilot_name')]
              .stringValue)
          .toUpperCase();
      String type = formName(form.type).toUpperCase();
      return '$id $rank$name - $type $dateText.pdf';
    } else if (form.type == f.FormType.lineTrain) {
      String dateText = form
          .fields[form.fields.indexWhere((e) => e.name == 'date_1')].stringValue
          .replaceAll(' ', '-');
      // DateTime date = form
      //     .fields[form.fields.indexWhere((e) => e.name == 'date_1')]
      //     .dateTimeValue!;
      // String dateText = DateFormat('yyyyMMdd').format(date);

      String id = idFormatted(form
          .fields[form.fields.indexWhere((e) => e.name == 'pilot_id')]
          .stringValue);
      String name = form
          .fields[form.fields.indexWhere((e) => e.name == 'pilot_name')]
          .stringValue
          .toUpperCase();
      String type = formName(form.type).toUpperCase();
      String time = DateFormat('kk:mm:ss').format(DateTime.now());
      return '$id $name - $type $dateText $time.pdf';
    } else if (form.type == f.FormType.lineCheck) {
      String dateText = DateFormat('yyyyMMdd').format(date);
      String time = DateFormat('kk:mm:ss').format(DateTime.now());
      String pilotName = form
          .fields[form.fields.indexWhere((e) => e.name == 'pilot_name')]
          .stringValue;
      return '${formName(form.type)}_${pilotName}_$dateText $time.pdf';
    } else {
      return '${form.id}.pdf';
    }
  }

  String idFormatted(String raw) {
    if (raw.length < 4) {
      return raw.padLeft(4, '0');
    } else if (raw.length > 4) {
      return raw.substring(raw.length - 4, raw.length);
    } else {
      return raw;
    }
  }

  String nameFormatted(String raw) {
    final splitted = raw.split(' ');
    String surname = '';
    if (splitted.length >= 2) {
      /// Name
      String name = splitted[0];

      /// Surname
      if (splitted[1].length >= 2) {
        surname = splitted[1].substring(0, 2);
        return '$name $surname.';
      } else if (splitted[1].length == 1) {
        surname = splitted[1];
        return '$name $surname.';
      } else {
        return '$name';
      }
    } else {
      return raw;
    }
  }

  /// Async function which saves feedback, parses [feedbackForm] parameters
  /// and sends HTTP GET request on [url]. On successful response, [callback] is called.
  Future submitForm(
      f.Form form, void Function(String, ErrorType) callback) async {
    downloadUrl = null;
    DateTime submitDate = DateTime.now();
    // double percentCompleted;
    String validateResult = '';
    if (!Authen.isSample) {
      await save(form);
      if (form.percentFilled() == 100) {
        if (await (Connectivity().checkConnectivity()) ==
            ConnectivityResult.none) {
          callback('No internet connection: Form moved to pending',
              ErrorType.noInternet);
          moveToPending(form);
        } else {
          try {
            // Utils.showInProgress(true);
            print('start pdf');
            //UPLOAD PDF TO GOOGLE STORAGE
            validateResult = form.validate();
            if (validateResult == '') {
              String name = pdfName(form, submitDate);
              List<int> pdfAsBytes =
                  await pdf.gen(form, (String response, ErrorType type) {
                callback(response, type);
              });
              final Directory directory =
                  await path_provider.getApplicationSupportDirectory();
              String path = directory.path;
              final File file = File('$path/$name');
              await file.writeAsBytes(pdfAsBytes, flush: true);
              TaskSnapshot snapshot = await storage
                  .ref()
                  .child(
                      "${folderName(form.type)}/${pdfName(form, submitDate)}")
                  .putFile(file);
              if (snapshot.state == TaskState.success) {
                downloadUrl = await snapshot.ref.getDownloadURL();
                form.pdfUrl = downloadUrl;
              } else {
                form.pdfUrl = 'error';
              }
              print('PDF UPLOADED.');
              print('START APPEND GOOGLE SHEET2');
              //UPLOAD JSON TO GOOGLE SHEET
              await http
                  .post(Uri.parse(formUrl(form.type)), body: form.formMap())
                  // body: form.lineCheckToMap())
                  .then((response) async {
                print(response.statusCode);
                if (response.statusCode == 302) {
                  var _url = response.headers['location'];
                  print(_url);
                  await http.get(Uri.parse(_url!)).then((response) {
                    forms[forms.indexWhere((e) => e.id == form.id)]
                        .submitDateTime = submitDate;
                    moveToComplted(form);
                    callback(kStatusSuccess, ErrorType.success);
                    print('APPEND GOOGLE SHEET SUCCESS');
                  });
                } else {
                  ///To keep error form in draft.
                  // moveToPending(form);
                  callback('Sheet error code: ${response.statusCode}',
                      ErrorType.other);
                }
              });
            } else {
              callback('ERROR: $validateResult', ErrorType.validate);
            }
          } catch (e) {
            ///To keep error form in draft.
            // moveToPending(form);
            // Utils.showInProgress(false);
            callback('ERROR: $e', ErrorType.other);
          }
        }
      } else {
        // Utils.showInProgress(false);
        callback(
            'ERROR: Please fill all required field', ErrorType.missingRequired);
      }
    } else {
      ///Mockup success message
      callback(kStatusSuccess, ErrorType.success);
    }
    // Utils.showInProgress(false);
  }

